    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        error="";
        if(!checkFileds()){
            JOptionPane.showMessageDialog(this, error, "Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Article article = new Article();
        article.setName(txtName.getText());
        article.setPrice(Double.parseDouble(txtPrice.getText()));
        article.setType(comboType.getSelectedItem().toString());
        try {
            Article returnArticle=ControllerUI.getInstance().saveArticle(article);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio artikal");
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti artikal: "+ex.getMessage(), "Message", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnSaveActionPerformed

