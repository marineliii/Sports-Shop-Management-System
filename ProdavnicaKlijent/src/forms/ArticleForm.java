    private boolean checkFileds() {
        boolean signal=true;
        if(Validator.getInstance().isEmpty(txtName.getText()) || Validator.getInstance().isEmpty(txtPrice.getText())){
            error+="Nisu popunjena sva polja\n";
            signal = false;
        }
        
        if(Validator.getInstance().isDouble(txtPrice.getText())){
            error+="Nije uneta numericka vrednost za cenu\n";
            signal = false;            
        }else if(Validator.getInstance().isLowerThanZero(Double.parseDouble(txtPrice.getText()))){
                    error+="Cena mora biti veca od 0\n";
                    signal = false;
        }
   
        
        return signal;
    }

    private void prepareView(int formMode) {
        switch (formMode) {
            case FormViewMode.ADD_MODE:
                btnChange.setEnabled(false);
                btnSave.setEnabled(true);
                btnUpdate.setEnabled(false);
                txtName.setEditable(true);
                txtPrice.setEditable(true);
                comboType.setEnabled(true);
                break;
            case FormViewMode.VIEW_MODE:
                btnChange.setEnabled(true);
                btnSave.setEnabled(false);
                btnUpdate.setEnabled(false);
                txtName.setEditable(false);
                txtPrice.setEditable(false);
                comboType.setEnabled(false);
                break;
            case FormViewMode.EDIT_MODE:
                btnChange.setEnabled(false);
                btnSave.setEnabled(false);
                btnUpdate.setEnabled(true);
                txtName.setEditable(true);
                txtPrice.setEditable(true);
                comboType.setEnabled(true);
                break;
            default:
                throw new AssertionError();
        }
    }

    private void setArticle() {
        txtName.setText(chosenArticle.getName());
        txtPrice.setText(String.valueOf(chosenArticle.getPrice()));
        comboType.setSelectedItem(chosenArticle.getType());
    }
    
    
