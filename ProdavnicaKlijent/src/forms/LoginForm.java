    private boolean checkFields() throws Exception {

        lblUsernameError.setText("");
        lblPasswordError.setText("");
        if (Validator.getInstance().isEmpty(txtUsername.getText())) {
            lblUsernameError.setForeground(Color.red);
            lblUsernameError.setText("Polje za unos korisnickog imena je prazno!");
            if (Validator.getInstance().isInBound(txtPassword.getPassword().length, 0, 4)) {
                lblPasswordError.setForeground(Color.red);
                lblPasswordError.setText("Sifra mora da sadrzi vise od 5 slova!");
            }
            return false;
        }
        if (Validator.getInstance().isInBound(txtPassword.getPassword().length, 0, 4)) {
            lblPasswordError.setForeground(Color.red);
            lblPasswordError.setText("Sifra mora da sadrzi vise od 5 slova!");
            return false;
        }

        return true;
    }
