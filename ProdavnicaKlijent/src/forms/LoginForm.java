    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            if(!checkFields()){
                return;
            }
            String username = txtUsername.getText().trim();
            String password = new String(txtPassword.getPassword());
            Worker worker = ControllerUI.getInstance().login(username, password);            
            if (worker != null) {
                JOptionPane.showMessageDialog(this, "Dobrodosao, " + worker.getFirstname());
                this.dispose();
                new MainForm(worker).setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this, "Uneti korisnik ne postoji");
            }

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Sistem ne moze da izvrsi autentifikaciju: " +ex.getMessage(), "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

