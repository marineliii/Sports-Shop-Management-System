    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!checkFields()){
            JOptionPane.showMessageDialog(this, "Nisu popunjena sva polja", "Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            file=new File("config/dbconfig.properties");
            Properties p = new Properties();
            p.load(new FileInputStream("config/dbconfig.properties"));
            p.setProperty("url", txtUrl.getText());
            p.setProperty("username", txtUser.getText());
            p.setProperty("password", txtPassword.getText());
            FileOutputStream out = new FileOutputStream(file);
            p.store(out, null);
            out.close();
            JOptionPane.showMessageDialog(this, "Uspesno promenjeno!");
            this.dispose();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FormSettings.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FormSettings.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

